-- "Figure 1.25: A three-dimensional bush-like structure" p. 26
-- Our implementation of ! is different, this system expects an undefined
-- "decrease" in width.  It's also unclear what method this system was supposed
-- to be using to reset stem colors, hence have added '(0) and inlined a few
-- other color markings to match the intent.
examplePlant 1 =
  lsystem $ do
    n 5
    theta 22.5
    axiom "A"
    productions
      [ ( "A"
        , "[ & '(0) F L A ] / / / / / [ & '(0) F ' ' L ! A ] / / / / / / / [ & '(0) F ' ' ' L ! A ]")
      , ("F", "S / / / / / F")
      , ("S", "F L")
      , ("L", "[ ' ^ ^ { - Fx + Fx + Fx - | - Fx + Fx + Fx } ]")
      ]
